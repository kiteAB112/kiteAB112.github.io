let activeCharacterId = atlas.activeCharacterId;
activeCharacterId = getInitialCharacterId();

renderAtlas();
bindImageDialog();
window.addEventListener("popstate", () => {
  activeCharacterId = getInitialCharacterId();
  renderAtlas();
});

function renderAtlas() {
  const character = getActiveCharacter();
  const works = getRelatedWorks(character);

  document.documentElement.style.setProperty("--character", character.color);
  document.documentElement.style.setProperty("--character-accent", character.accent);

  renderHero(character, works);
  renderSpotlightSwitcher(character);
  renderAtlasStrip(character, works);
  renderProfile(character);
  renderQuotes(character);
  renderGallery(character);
  renderWorks(works);
  updateCounters(character, works);
  bindSpotlightButtons();
  bindGalleryItems();
}

function getInitialCharacterId() {
  const params = new URLSearchParams(window.location.search);
  const requestedId = params.get("character");
  const exists = atlas.characters.some((character) => character.id === requestedId);

  return exists ? requestedId : atlas.activeCharacterId;
}

function getActiveCharacter() {
  return (
    atlas.characters.find((character) => character.id === activeCharacterId) ??
    atlas.characters[0]
  );
}

function getRelatedWorks(character) {
  return atlas.works.filter((work) => character.workIds.includes(work.id));
}

function renderHero(character, works) {
  document.querySelector("#hero-media").innerHTML = `
    <img
      src="${escapeAttr(character.heroImage)}"
      alt="${escapeAttr(character.name)}背景图"
      style="object-fit: ${escapeAttr(character.heroFit ?? "cover")}; object-position: ${escapeAttr(character.heroPosition ?? "center")};"
    />
  `;

  document.querySelector("#hero-content").innerHTML = `
    <p class="eyebrow">${escapeHtml(character.title)}</p>
    <h1 id="page-title">${escapeHtml(character.name)}</h1>
    <p class="hero-roman">${escapeHtml(character.kana)}</p>
    <p class="lead">「${escapeHtml(character.catchphrase)}」</p>
    <div class="hero-tags">
      <span>${escapeHtml(character.work)}</span>
      <span>${escapeHtml(character.type)}</span>
      <span>${works.length} Works</span>
    </div>
    <div class="hero-actions">
      <a class="button primary" href="#profile">查看档案</a>
      <a class="button ghost" href="#gallery">浏览画廊</a>
    </div>
  `;
}

function renderAtlasStrip(character, works) {
  document.querySelector("#atlas-strip").innerHTML = `
    <article>
      <span>Active</span>
      <strong>${escapeHtml(character.name)}</strong>
    </article>
    <article>
      <span>Tags</span>
      <strong>${character.tags.length}</strong>
    </article>
    <article>
      <span>Images</span>
      <strong>${character.gallery.length}</strong>
    </article>
    <article>
      <span>Works</span>
      <strong>${works.length}</strong>
    </article>
  `;
}

function renderSpotlightSwitcher(activeCharacter) {
  document.querySelector("#spotlight-switcher").innerHTML = atlas.characters
    .map(
      (character) => `
        <button
          class="spotlight-button ${character.id === activeCharacter.id ? "is-active" : ""}"
          type="button"
          data-character-id="${escapeAttr(character.id)}"
          style="--accent: ${escapeAttr(character.color)}"
          aria-pressed="${character.id === activeCharacter.id ? "true" : "false"}"
        >
          <img src="${escapeAttr(character.cover)}" alt="" loading="lazy" />
          <span>
            <strong>${escapeHtml(character.name)}</strong>
            <small>${escapeHtml(character.work)}</small>
          </span>
        </button>
      `,
    )
    .join("");
}

function renderProfile(character) {
  document.querySelector("#active-work").textContent = character.work;
  document.querySelector("#profile-portrait").innerHTML = `
    <img
      src="${escapeAttr(character.portrait)}"
      alt="${escapeAttr(character.name)}"
      style="object-fit: ${escapeAttr(character.portraitFit ?? "cover")};"
    />
  `;

  document.querySelector("#character-profile").innerHTML = `
    <div class="profile-heading">
      <span>${escapeHtml(character.kana)}</span>
      <h3>${escapeHtml(character.name)} · ${escapeHtml(character.title)}</h3>
    </div>
    <div class="profile-text">
      ${character.intro.map((text) => `<p>${escapeHtml(text)}</p>`).join("")}
    </div>
    <div class="stat-grid">
      ${character.stats
        .map(
          ([label, value]) => `
            <article class="stat-card">
              <span>${escapeHtml(label)}</span>
              <strong>${escapeHtml(value)}</strong>
            </article>
          `,
        )
        .join("")}
    </div>
    <div class="tag-row profile-tags">
      ${character.tags.map((tag) => `<span>${escapeHtml(tag)}</span>`).join("")}
    </div>
  `;
}

function renderQuotes(character) {
  document.querySelector("#quote-grid").innerHTML = character.quotes
    .map(
      (quote, index) => `
        <article class="quote-card">
          <span>${String(index + 1).padStart(2, "0")}</span>
          <p>「${escapeHtml(quote)}」</p>
        </article>
      `,
    )
    .join("");
}

function renderGallery(character) {
  document.querySelector("#gallery-grid").innerHTML = character.gallery
    .map(
      ([src, alt, format], index) => `
        <button
          class="gallery-item ${index === 0 ? "is-featured" : ""} gallery-${escapeAttr(format)}"
          type="button"
          data-src="${escapeAttr(src)}"
          data-alt="${escapeAttr(alt)}"
          aria-label="${escapeAttr(alt)}"
        >
          <img src="${escapeAttr(src)}" alt="${escapeAttr(alt)}" loading="lazy" />
          <span>${String(index + 1).padStart(2, "0")}</span>
        </button>
      `,
    )
    .join("");
}

function renderWorks(works) {
  document.querySelector("#work-grid").innerHTML = works
    .map(
      (work) => `
        <a class="work-card" href="${escapeAttr(work.url)}" target="_blank" rel="noopener noreferrer">
          <img src="${escapeAttr(work.image)}" alt="${escapeAttr(work.title)}" loading="lazy" />
          <div>
            <div class="work-meta">
              <span>${escapeHtml(work.year)}</span>
              <span>${escapeHtml(work.format)}</span>
            </div>
            <h3>${escapeHtml(work.title)}</h3>
            <p>${escapeHtml(work.note)}</p>
          </div>
        </a>
      `,
    )
    .join("");
}

function updateCounters(character, works) {
  document.querySelector("#quote-count").textContent =
    `${character.quotes.length} saved`;
  document.querySelector("#gallery-count").textContent =
    `${character.gallery.length} images`;
  document.querySelector("#work-count").textContent = `${works.length} linked`;
}

function bindSpotlightButtons() {
  document.querySelectorAll(".spotlight-button").forEach((button) => {
    button.addEventListener("click", () => {
      selectCharacter(button.dataset.characterId);
    });
  });
}

function bindGalleryItems() {
  document.querySelectorAll(".gallery-item").forEach((item) => {
    item.addEventListener("click", () => {
      openImageDialog(item.dataset.src, item.dataset.alt);
    });
  });
}

function selectCharacter(characterId) {
  if (characterId === activeCharacterId) {
    return;
  }

  activeCharacterId = characterId;
  updateCharacterUrl(characterId);
  renderAtlas();
}

function updateCharacterUrl(characterId) {
  const url = new URL(window.location.href);
  url.searchParams.set("character", characterId);
  window.history.pushState({ characterId }, "", url);
}

function bindImageDialog() {
  const dialog = document.querySelector("#image-dialog");
  const closeButton = document.querySelector(".dialog-close");

  closeButton.addEventListener("click", () => {
    dialog.close();
  });

  dialog.addEventListener("click", (event) => {
    if (event.target === dialog) {
      dialog.close();
    }
  });
}

function openImageDialog(src, alt) {
  const dialog = document.querySelector("#image-dialog");
  const dialogImage = document.querySelector("#dialog-image");

  dialogImage.src = src;
  dialogImage.alt = alt;

  if (typeof dialog.showModal === "function") {
    dialog.showModal();
  }
}

function escapeHtml(value) {
  return String(value)
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function escapeAttr(value) {
  return escapeHtml(value).replaceAll("`", "&#096;");
}
